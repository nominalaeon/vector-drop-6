
export default {
	name: 'TitleCardComponent',
  props: {
    height: Number,
    width: Number
  },

  data: function buildData() {
    return {
      bg: '',
      fill: '',
      stroke: '',
      titleCard: {
        fill: _buildTitleCardFill(),
		    head: _buildTitleCardHead(),
        stroke: _buildTitleCardStroke()
      }
    }
  },

  mounted: function onMounted() {
    Object.assign(this, selectElements(this.$el));

    console.info('TitleCard initialized', this);

    return animate(this.bg, this.fill, this.head, this.stroke);
  }
}

/**
 * General methods
 */

function animate(bg, fill, head, stroke) {
  var trace = new TimelineLite({ repeat: 0 });
  var traceDur = 6;
  var tracePos = `-=${traceDur / 2}`;
  var fillDur = 1.5;
  var fillPos = `-=${traceDur}`;

  var configRoughEase = {
    clamp: false,
    points: 50,
    randomize: true,
    strength: 2,
    taper: 'none',
    template: Circ.easeIn
  };

	TweenLite.to(head, traceDur, {
		ease: Expo.easeInOut,
		opacity: 1
	});

  return stroke && fill ? trace
    .fromTo(stroke, traceDur, {
      drawSVG: '0% 0%',
      opacity: 1,
      stroke: '#FF1AA2'
    }, {
      drawSVG: '0% 100%',
      ease: Expo.easeInOut,
      opacity: 1,
    }, tracePos)
    .to(stroke, traceDur, {
      drawSVG: '100% 100%',
      ease: Expo.easeOut,
      opacity: 0,
      // stroke: '#000'
    }, tracePos)
    .to(fill, fillDur, {
      opacity: 1,
      ease: RoughEase.ease.config(configRoughEase)
    }, fillPos)
    .to(bg, traceDur, {
      ease: Expo.easeOut,
      opacity: 1
    }, tracePos) : {};
}

function selectElements(el) {
  return {
    bg: el.querySelector('.vd6-title-card__path--bg'),
		fill: el.querySelector('.vd6-title-card__path--fill'),
    head: el.querySelector('.vd6-title-card__path--head'),
    stroke: el.querySelector('.vd6-title-card__path--stroke path')
  };
}

